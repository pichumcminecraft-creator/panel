namespace App\Services\Chatbot\Tools;

use App\Chat\Server;
use App\Chat\Allocation;
use App\Helpers\ServerGateway;

/**
 * Tool to get server allocations.
 */
class GetServerAllocationsTool implements ToolInterface
{
    public function execute(array $params, array $user, array $pageContext = []): mixed
    {
        // Get server identifier
        $serverIdentifier = $params['server_uuid'] ?? $params['server_name'] ?? null;
        $server = null;

        // If no identifier provided, try to get server from pageContext
        if (!$serverIdentifier && isset($pageContext['server'])) {
            $contextServer = $pageContext['server'];
            $serverUuidShort = $contextServer['uuidShort'] ?? null;

            if ($serverUuidShort) {
                $server = Server::getServerByUuidShort($serverUuidShort);
            }
        }

        // Resolve server if identifier provided
        if ($serverIdentifier && !$server) {
            $server = Server::getServerByUuid($serverIdentifier);

            if (!$server) {
                $server = Server::getServerByUuidShort($serverIdentifier);
            }

            if (!$server) {
                $servers = Server::searchServers(
                    page: 1,
                    limit: 10,
                    search: $serverIdentifier,
                    ownerId: $user['id']
                );
                if (!empty($servers)) {
                    $server = $servers[0];
                }
            }
        }

        if (!$server) {
            return [
                'success' => false,
                'error' => 'Server not found. Please specify a server UUID or name, or ensure you are viewing a server page.',
            ];
        }

        // Verify user has access
        if (!ServerGateway::canUserAccessServer($user['uuid'], $server['uuid'])) {
            return [
                'success' => false,
                'error' => 'Access denied to server',
            ];
        }

        // Get allocations
        $allocations = Allocation::getByServerId($server['id']);

        // Mark which allocation is primary
        foreach ($allocations as &$allocation) {
            $allocation['is_primary'] = ((int) $allocation['id'] === (int) $server['allocation_id']);
        }

        // Get server's allocation limit
        $allocationLimit = (int) ($server['allocation_limit'] ?? 100);
        $currentAllocations = count($allocations);

        return [
            'success' => true,
            'server_name' => $server['name'],
            'allocation_limit' => $allocationLimit,
            'current_allocations' => $currentAllocations,
            'can_add_more' => $currentAllocations < $allocationLimit,
            'primary_allocation_id' => $server['allocation_id'],
            'allocations' => $allocations,
        ];
    }

    public function getDescription(): string
    {
        return 'Get all allocations for a server. Returns allocation list, limits, and which allocation is primary.';
    }

    public function getParameters(): array
    {
        return [
            'server_uuid' => 'Server UUID (optional, can use server_name instead)',
            'server_name' => 'Server name (optional, can use server_uuid instead)',
        ];
    }
}
